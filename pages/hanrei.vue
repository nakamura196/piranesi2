      <h4>
        <a name="kana">地名・人名などの固有名詞の和文表記について</a>
      </h4>
      <p></p>
      <ol>
        <li>
          カタカナ表記の元となる言語の選択について
          <br />
          <p>
            (1-a)
            地名・人名の固有名詞については，原則として可能な限り「現地語読みに基づいたカタカナ表記」の方針を取る。ここにいう「現地語」とは，本データベースの扱う題材が地理的のみならず時代的広がりを持っていることを考慮して，より正確に言えば「現地・当代語読み」とも言うべき，拡大された意味での「現地語読み」である。
          </p>

          <p>
            (1-b)
            よって，本データベースで主に扱われるイタリア半島における人物・地名等については，ローマ時代から存在する人物・土地の名称はラテン語の表記に従って，それ以降のものについてはジョヴァンニ・バッティスタ・ピラネージによるイタリア語の表記に従って，カタカナにする。また古代ギリシャに由来する地名・人名については，古典ギリシャ語を元にする。
          </p>

          <p>
            (1-c)
            たとえ古代ギリシャに由来する地名・人名であっても，ローマ世界に密着した形で使用される場合は，ラテン語形を採用した。例として，イタリア半島における神殿において，古代ギリシャ起源と目される神・英雄が完全にローマ化されている場合などである。
            <br />例：ユッピテル・スタトル神殿，ポルティクス・オクタウィアエ内にあるユノ神殿
          </p>

          <p>
            (1-d)
            ローマ時代から存在する古代の名称とは別に，後代の名称が広く慣例として通用している場合，古代の名称［一般的通称名］のように，［
            ］を付して併記した。また，ローマ時代以降の地名の呼称であっても，別の形で古代の名称が存在する場合，イタリア語読み名称《ラテン語読み名称》のように，《
            》を付けて併記した。
          </p>

          <p>
            (1-e)
            地名・人名の範疇は，実在するもののみならず，神話上の地名，人名，英雄名，および神の名前等をも含むものとする。
          </p>
        </li>
        <li>
          カタカナ表記における諸規則
          <br />
          <p>
            (2-a)
            古典ギリシャ語・ラテン語においては，長母音を示すための音引き「ー」は削除し，促音「ッ」は
            ll，rrの前も含め全て採用する。有気子音については，th，khは[t]，[k]に準じて，phについては[f]に準じて，音写する。
          </p>

          <p>
            (2-b)
            イタリア語における音引きとアクセントの関係については，以下のようにする。
          </p>
          <ul type="disc">
            <li>
              後ろから第二音節にアクセントのあるものは原則として音引きを入れる。
            </li>
            <li>
              ただし，
              二重子音の前は音引きを削除し，連続する母音にアクセントがある場合は慣用に従う。
            </li>
            <li>
              後ろから第二音節以外にアクセントのあるものは音引きを削除。（例：
              Stefano「ステファノ」）
            </li>
          </ul>
          <p></p>
        </li>
        <li>
          例外事項
          <br />
          <p>
            一般的にとりわけなじみの深い下記の地名・人名については，慣例として通用している名称を採用した。
          </p>
          <ul type="none">
            地名：
            <br />
            <li>ローマ（×ロマ：cf. 2-a)</li>
            <li>ギリシャ（cf. 1-a）</li>
            <li>
              ナイル（cf. 1-a）
              <br />人名：
              <br />
            </li>
            <li>アポロ／アポロン（×アポッロ／アポッロン：cf. 2-a）</li>
            <li>フローラ（×フロラ：cf. 2-a）</li>
            <li>ヘラクレス（×ヘルクレス：cf. 1-c）</li>
            <li>メドゥーサ（×メドゥサ：cf. 2-a）</li>
          </ul>
          <p></p>
        </li>
