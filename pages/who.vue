                <br />11月8日、ヴェネチアのサン・モーゼ教会堂で洗礼を受ける。
              </v-card-text>
            </v-card>
          </v-timeline-item>
        </v-timeline>

        <p>
          —1720年
          <br />10月4日、ヴェネト州メストレ近郊のモリアーノで、アンジェロ・ピラネージとラウラ・ルッケージの間にジョヴァンニ・バッティスタ・ピラネージ誕生。
          <br />11月8日、ヴェネチアのサン・モーゼ教会堂で洗礼を受ける。
        </p>
        <p>
          —1730〜40年頃
          <br />叔父で建築家かつ技師であるマッテオ・ルッケージ、及び建築家のジョヴァンニ・スカルファロットに学んだとされる。また、カルロ・ヅッキに遠近法を、カルトジオ会修道士でローマ史とラテン語を研究している、兄のアンジェロに学び、年配のフェルディナンド・ビビエーナ、またおそらくその家族、さらにドメニコそしてジュゼッペ・ヴァレリアーニからは劇場設計図を学んだと思われる。
        </p>
        <p>
          —1740年
          <br />マルコ・フォスカリーニ大使は教皇べネディクトゥス14世の宮廷を訪れ、その随行として初めてローマに赴く。
        </p>
        <p>
          —1740〜43年
          <br />ニコラ・ジョッベを介して、建築家のルイジ・ヴァンヴィテッリとニコラ・サルヴィと会う。またヴァチカン図書館司書のジョヴァンニ・ボッターリとも面識を持つ。ジュゼッペ・ヴァージの工房で版画を研究するが、彼と反りが合わなくなる。
          <br />ヴァージを介して、「オペレ・ヴァリエ」（ <i>Opere Varie</i>,
          1750）でピラネージの肖像を描くフェリーチェ・ポランツァーニと出会う。ナポリとエルコラーノの発掘を訪問し、そしてピラネージを古代遺跡の記録に関心を持たせるさせるポルティッチ美術館館長カルロ・パデルニと出会う。
        </p>
        <p>
          —1743年
          <br />ニコラ・ジョッベに献じられた12葉の「第１部 建築と遠近法」（
          <i>Prima Parte di Architetture, e Prospettive</i>）をローマで刊行。
        </p>
        <p>
          —1744年
          <br />ヴェネチアに滞在し、そこで恐らくティエポロの工房に通い、幾つかの絵画作品の依頼を受ける。
        </p>
        <p>
          —1744〜45年
          <br />版画出版者ジュゼフ・ワーグナーの薦めでローマに戻る。
          <br />フランス・アカデミー近辺のコルソ通りに居住する。
        </p>
        <p>
          —1744〜46年
          <br />「テヴェレ川流域の地図」（
          <i>Pianta del Corso del Tevere</i>）でカルロ・ノッリと協力する。
        </p>
        <p>
          —1745〜50年頃
          <br />135葉の「ローマの景観」（
          <i>Vedute di Roma</i>）、4葉の「グロテスキ」（
          <i>Grotteschi</i>)、14葉の「牢獄の奇想」（
          <i>Invenzioni capric(ciose) de Carceri</i
          >）、11葉の「古代ローマの墳墓」（ <i>Camere sepolcrali</i>：最終的に
          <i>Le Antichità Romane</i> に組み入れられる）を出版する。
        </p>
        <p>
          —1748年
          <br />28葉の「共和政及び帝政初期ローマの遺跡」（
          <i
            >Antichità Romane de' Tempi della Repubblica, e de'primi
            Imperatori</i
          >）を出版する。ジャン・バッティスタ・ノッリと「新ローマ地図」（
          <i>Nuova Pianta di Roma</i>）を協力する。
        </p>
        <p>
          —1750年
          <br />ジョヴァンニ・ブシャールが、「オペレ・ヴァリエ」（
          <i>Opere Varie</i
          >）と題するピラネージの作品集を出版する。そこには類似した様式作品の「第1部—建築と景観」（
          <i>Prima Parte di Architetture, e Prospetteve</i>）と「グロテスキ」（
          <i>Grotteschi</i>）が含まれている。
        </p>
        <p>
          —1751年
          <br />ジョヴァンニ・ブシャールが、「ローマの壮麗」（
          <i>Le Magnificenze di Roma</i
          >）と題するピラネージの別の作品集を出版する。そこには、「ローマの景観」（
          <i>Vedute di Roma</i>）の34葉と「牢獄の奇想」（
          <i>Invenzioni capric(ciose) de Carceri</i
          >）の第2版、そして「共和政及び帝政初期のローマ遺跡」（
          <i>Le Antichità Romane de' Tempi della Repubblica</i
          >）の第2版が含まれている。この頃、ピラネージはシャルルモン卿と出会い、「ローマの遺跡」（
          <i>Le Antichità Romane</i>）の出版を合意する。
        </p>
        <p>
          —1752年
          <br />庭師コルシーニ家の娘アンジェラ・パスクイーニと結婚する。「ローマの遺跡」（
          <i>Le Antichità Romane</i
          >）の出版の為に、彼女の持参金をその銅板に投資する。
        </p>
        <p>
          —1753年
          <br />ジョヴァンニ・ブシャールは、10葉の「オクタウィアヌス・アウグストゥスの戦勝記念碑」（
          <i>Trofei di Ottaviano Augusto innalzati per la Vittoria ad Actium</i
          >）を出版する。
        </p>
        <p>
          —1755年頃
          <br />娘ラウラ誕生。ロバート・アダムと出会う。
        </p>
        <p>
          —1756年
          <br />218葉の「ローマの遺跡（4巻）」（
          <i>Le Antichità Romane</i>）を出版する。
        </p>
        <p>
          —1757年
          <br />2月24日、ロンドン考古学会名誉会員に選ばれる。
          <br />シャルルモン卿の「ローマの遺跡」における経営的失策に対して失意を抱き、彼に宛てた公開状「シャルルモン卿への弁明書」（
          <i
            >Lettere di Giustificazione scritte a Milord Charlemont e a'di lui
            agenti di Roma</i
          >）を出版する。
        </p>
        <p>
          —1758〜9年
          <br />息子フランチェスコ誕生。
        </p>
        <p>
          —1760年頃
          <br />過去のシリーズを再版するが、特に「ローマの景観」（
          <i>Vedute di Roma</i>）や「想像による牢獄」（
          <i>Carceri d' Invenzione</i>）と改題することになる「牢獄の奇想」（
          <i>Invenzioni capric(ciose) di Carceri：2葉追加</i>）を手直しする。
        </p>
        <p>
          —1761年
          <br />2月2日、サン・ルカ・アカデミー会員に選出される。3月までにサンタ・トリニタ・デイ・モンティ近くのフェリーチェ通り（現システィーナ通り）にあるトマーティ館に居住する。
          <br />「ローマの景観」（ <i>Vedute di Roma</i>）と「想像による牢獄」（
          <i>Carceri d' Invenzione</i
          >）を再版。さらに、「シャルルモン卿への弁明書」（
          <i>Lettere di Giustificazione scritte a Milord Charlemont</i
          >）からの版画を含む「オペレ・ヴァリエ」（
          <i>Opere Varie</i
          >）を再版。また、25葉の「ユリウス水道の貯水槽の遺構」（
          <i>Le Rovine del Castello dell' Aqua Guilia</i
          >）や40葉の「ローマの壮麗なる建築」（
          <i>Della Magnificenza ed architettura de' Romani</i
          >）も出版。自身の全版画の作品目録を出版準備し始める。
        </p>
        <p>
          —1762年
          <br />ロバート・アダムに献じられた52葉の「古代ローマのカンプス・マルティウス」（
          <i>Il Campo Marzio dell' Antica Roma</i
          >）と7葉の「カピトリヌス丘の石碑、すなわちローマの執政官表および凱旋将軍表」（
          <i>Lapides Capitolini sive Fasti Consulares</i
          >）、そして12葉の「アルバノ湖の放水路」（
          <i>Descrizione e Disegno dell' Emissario del Lago Albano</i
          >）を出版する。
        </p>
        <p>
          —1763〜4年
          <br />キウージとコルネートを訪れる。
          <br />サン・ジョヴァンニ・イン・ラテラノ聖堂の主祭壇の再建と内陣の修理を依頼され、そのデザインを手がけるが、実現せず。
        </p>
        <p>
          —1764年
          <br />32葉の「アルバノとカステル・ガンドルフォの遺跡」（
          <i>Antichità d' Albano e di Castel Gandolfo</i
          >）、24葉の「古代都市コラ遺跡」（
          <i>Antichità di Cora</i>）、そして4葉の「グエルチーノのデッサン集」（
          <i
            >Raccolta di alcuni disegni del Barbieri da Cento detto Il
            Guercino</i
          >）を出版する。
          <br />サンタ・マリア・デル・プリオラート教会堂の修復を枢機卿ジョヴァンニ・バッティスタ・レッツォーニコから依頼される。
        </p>
        <p>
          —1764〜66年
          <br />マルタ騎士団広場の設計に従事する。
        </p>
        <p>
          —1765年
          <br />「マリエット氏の書簡に関する省察」（
          <i>Osservazioni sopra la lettere de M. Mariette</i>）を出版する。
        </p>
        <p>
          —1765年以後
          <br />28葉の「古代ローマによる凱旋門およびその建造物の景観」（
          <i
            >Alcune Vedute di Archi Trionfali ed altri monumenti inalzati dà
            Romni</i
          >）を出版する。
        </p>
        <p>
          —1766年
          <br />サンタ・マリア・デル・プリオラート・スラヴェンティーノ教会堂の仕事を終える。
        </p>
        <p>
          —1767年頃
          <br />クイリナーレにある枢機卿レッツォーニコのアパルタメントの装飾に従事する。
        </p>
        <p>
          —1767年
          <br />1月16日、教皇クレメンス13世より、金の拍車（Cavaliere degli
          Speroni d' Oro）の騎士の称号を受ける。
        </p>
        <p>
          —1768年頃
          <br />ピラネージが生涯を通じて手がける「古代の容器、燭台、墓標、棺、鼎、ランプ、および装飾品」（
          <i
            >Vasi, candelabri, cippi, sarcofagi, tripodi, lucerne ed ornamenti
            antichi</i
          >）の単品版画シリーズを開始する。
        </p>
        <p>
          —1769年
          <br />73葉の「暖炉の様々な装飾と建築部分の様々な装飾」（
          <i
            >Diverse Maniere d' adornare i cammini ed ogni altra parte degli
            edifizi</i
          >）を出版する。
        </p>
        <p>
          —1772年
          <br />ピオ・バレストラに献じるモニュメント構想で、サン・ルカ・アカデミーと不仲になる。
        </p>
        <p>
          —1774〜79年
          <br />「トラヤヌス帝の記念柱およびマルクス・アウレリウス帝の記念柱」（
          <i
            >Trofeo o sia Magnifica Colonna Coclide di marmoノove si veggono
            scolpite le due guerre daciche fatte da Trajano e Colonna Antonia</i
          >）を出版する。
        </p>
        <p>
          —1777年頃
          <br />パエストゥムを訪れ、神殿の大素描17葉を制作する。
        </p>
        <p>
          —1778年
          <br />「ローマとカンプス・マルティウスの地図」（
          <i>Pianta di Roma e del Campo Marzio</i
          >）と114葉の「古代の容器、燭台、墓標、棺、鼎、ランプ、および装飾品」（
          <i
            >Vasi, candelabri, cippi, sarcofagi, tripodi, lucenrne, ed ornamenti
            antichi</i
          >）を出版する。
          <br />息子フランチェスコの協力と共に「パエストゥムの古代都市に残る様々な光景」（
          <i
            >Différentes vues de quelques Restes de l' ancienne Ville de
            Pesto</i
          >）の素描を元に17葉を版刻する。
          <br />11月9日、ローマの自宅で死去。サンタ・マリア・デル・プリオラート教会堂に埋葬される。
        </p>
        <p>
          —1778〜9年
          <br />ロンドンで、ロバート・アダムの素描に倣ってピラネージが版刻した4葉（おそらく1764年の版）を「建築作品（2巻）」（
          <i>The Works in Architecture</i
          >）として、ロバート、そしてジェームス・アダムの手によって出版される。
        </p>
        <p>
          —1779〜98年
          <br />ピラネージの作品は家族と工房によって出版され続ける。
          <br />「古代神殿集」（ <i>Raccolta dei Tempi Antichi</i>,
          1780年）、「ハドリアヌス帝のヴィッラに残る建造物の地図」（
          <i>Pianta delle fabbriche esistente nella Villa Adriana</i>,
          1781年）、「エルコラノの劇場」（ <i>Il Teatro d' Ercolano</i>,
          1783年）、「スキピオ家の墓所」（ <i>Monumenti degli Scipioni</i>,
          1785年）、「ローマの最も美しい彫刻集」（
          <i>Collection des plus belles Statues de Rome</i>,
          1786年）、「第2部—古代神殿集」（
          <i>Seconda Parte de' Tempi Antichi</i>, 1790年）。
        </p>
        <p>
          —1792年
          <br />ピラネージの作品目録が出版される。
        </p>
        <p>
          —1799年
          <br />フランチェスコは政治的意図によりパリに移住し、そこで版画活動を続ける。
        </p>
        <p>
          —1800年
          <br />フランチェスコは父の作品を再版する。
        </p>
        <p>
          —1835〜9年
          <br />出版者フィルマン・ディドはピラネージの銅板を購入し、全作品集を再版する。
        </p>
        <p>
          —1839年
          <br />教皇グレゴリウス16世のために、枢機卿アントニオ・トスティは、ピラネージの銅板を購入し、その結果それらは最終的にローマに戻ることになる。
        </p>
