        <h3>Giovanni Battista Piranesi: Dati biografici</h3>
        <p>
          ―1720<br />
          Il 4 ottobre nasce da Angelo Piranesi e Laura Lucchesi a Mogliano
          presso Mestre in Veneto.<br />
          8 novembre: battezzato in San Mosé a Venezia.
        </p>
        <p>
          ―c. 1730 ～ 40<br />
          Notizie dei suoi studi con lo zio Matteo Lucchesi, ingegnere e
          architetto, con Giovanni Scalfarotto architetto, con Carlo Zucchi da
          cui avrebbe appreso prospettiva ed incisione, nonché con il fratello
          Angelo, monaco certosino che gli avrebbe insegnato storia romana e
          latino. Il disegno di scena dovrebbe essergli stato insegnato o dall'
          anziano Ferdinando Bibbiena o da membri della di lui famiglia o da
          Domenico e Giuseppe Valeriani.
        </p>
        <p>
          ―1740<br />
          Parte per Roma una prima volta, con Marco Foscarini, Ambasciatore di
          Venezia alla corte di Papa Benedetto XIV.
        </p>
        <p>
          ―1740 ～ 43<br />
          Presentato da Nicola Giobbe, si incontra con gli architetti Luigi
          Vanvitelli e Nicola Salvi e fa la conoscenza anche di Giovanni
          Bottari, un bibliotecario del Vaticano. Studia incisione nella bottega
          di Giuseppe Vasi, ma si viene a trovare in disaccordo. Presso Vasi,
          incontra Felice Polanzani che inciderà il suo ritratto pubblicato
          nalle
          <i>Opere Varie</i>, 1750. Visita Napoli, gli scavi de Ercolano ed il
          Museo di Portici, incontrandone il direttore Carlo Paderni che gli
          inculca l' interesse per i monumenti antichi. <br />
        </p>
        <p>
          ―1743<br />
          A Roma, pubblica <i>Prima Parte di Architetture, e Prospettive</i> (
          12 fogli ), dedicata a Nicola Giobbe.
        </p>
        <p>
          ―1744<br />
          È a Venezia dove forse frequenta la bottega di G. B. Tiepolo e riceve
          qualche incarico pittorico.
        </p>
        <p>
          ―1744 ～ 45<br />
          Torna a Roma per consiglio di Joseph Wagner, direttore di una bottega
          di incisione. Trova alloggio al Corso, non lontano dall' Accademia di
          Francia.
        </p>
        <p>
          ―1744 ～ 46<br />
          Collabora con Carlo Nolli alla <i>Pianta del Corso del Tevere.</i>
        </p>
        <p>
          ―c. 1745 ～ 50<br />
          Escono una prima edizione di <i>Vedute di Roma</i> ( 135 fogli ),
          <i>Grotteschi</i> ( 4 fogli ), la prima edizione di
          <i>Invenzioni capric(ciose) de Carceri</i> ( 14 fogli ) e
          <i>Camere sepolcrali </i> ( 11 fogli : i quali poi sarenno incorporati
          in <i>Le Antichità Romane</i>).
        </p>
        <p>
          ―1748<br />
          Pubblica
          <i
            >Antichità Romane de' Tempi della Repubblica, e de'primi
            Imperatori</i
          >
          ( 28 fogli ). Collabora con G. B. Nolli, alla
          <i>Nuova Pianta di Roma</i>.
        </p>
        <p>
          ―1750<br />
          Giovanni Bouchard pubblica una collezione di opere di Piranesi,
          intitolata <i>Opere Varie</i> in cui include
          <i>Prima Parte di Architetture, e Prospetteve</i> alcune altre stampe
          in uno stile simile ed anche <i>Grotteschi</i>.
        </p>
        <p>
          ―1751<br />
          Giovanni Bouchard pubblica un' altra collezione di opere di Piranesi,
          intitolata <i>Le Magnificenze di Roma</i>, in cui include 34 fogli di
          <i>Vedute di Roma</i>, la seconda edizione di
          <i>Invenzioni capric(ciose) de Carceri</i>, e la seconda edizione di
          <i>Le Antichità Romane de' Tempi della Repubblica</i>. Intorno a
          questa data, incontra Viscount Charlemont con il quale concorda una
          edizione di <i>Le Antichità Romane</i>.
        </p>
        <p>
          ―1752<br />
